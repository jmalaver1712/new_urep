								<h5>Lineamientos Generales y Opciones de Acreditación</h5>
								De conformidad con el Artículo 39 del Reglamento Estudiantil de la Corporación Universitaria Republicana, se constituye como requisito para optar el título de pregrado, contar con suficiencia en la lengua extranjera, sin perjuicio de otros, de conformidad con los siguientes lineamientos:
								<ul>
									<li><b>*</b> La Corporación por intermedio de cada facultad realizará al estudiante que lo solicite un examen de inglés a través del cual se definirá su nivel de suficiencia en el idioma</li>
									<li><b>*</b> De acuerdo con los resultados del examen, la Corporación indicará el nivel a partir del cual el estudiante debe iniciar o continuar su formación en inglés</li>
									<li><b>*</b> Después de aprobados los niveles de inglés ofrecidos por la Corporación para cada programa de pregrado, el estudiante deberá costearse los demás cursos necesarios para lograr las competencias requeridas, bien sea en la Corporación o fuera de ella. Si es fuera de ella, deberá presentar el certificado de una entidad reconocida oficialmente  y se realizará examen de suficiencia por la Corporación Universitaria Republicana</li>
									<li><b>*</b> El estudiante que no acredite las competencias exigidas no podrá optar el título hasta tanto no cumpla con las mismas</li>
									<li><b>*</b> La Corporación Universitaria Republicana establece el inglés como segunda lengua extranjera. Sin embargo, este requisito será satisfecho por estudiantes que acrediten suficiencia en otro idioma diferente al inglés cuando se trate de un nivel de conocimiento en dicho idioma que sea comparable al nivel de conocimiento de inglés aquí requerido:</li>
									<li><b>*</b> A partir de Enero 1 de 2019, todo estudiante de la Corporación Universitaria Republicana para optar por el título de pregrado de los programas académicos de Finanzas y Comercio Internacional, Ingeniería Industrial, Ingeniería de Sistemas, y aquellos que se crearen y pertenezcan a las Ciencias Administrativas o de Negocios, deberán acreditar suficiencia en una tercera lengua extranjera que sea acreditable dentro de los términos del presente comunicado. Los siguientes idiomas se encuentran disponibles y son aceptables para ser acreditados como tercera lengua extranjera dentro de los términos del presente comunicado: Francés, Alemán, Portugués, Italiano, Japonés o Mandarín.</li>
									<li><b>*</b> La certificación de lengua extranjera inglés o de otra lengua extranjera diferente al mismo es obligatoria para los estudiantes de pregrado que ingresaron o reingresaron bajo el sistema de créditos académicos</li>
									<li><b>*</b> Los exámenes de acreditación internacional y los puntajes mínimos que reconoce la Corporación Universitaria Republicana para acreditar los niveles B1 en inglés del Marco Común Europeo (MCE) son:
										<ul>
