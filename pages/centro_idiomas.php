								<h5> Procedimiento para Acreditar un Segundo Idioma </h5>
								Los estudiantes de pregrado de la Corporación Universitaria Republicana pueden escoger cualquiera de las opciones de acreditación anteriormente mencionas anteriormente (Examen de Acreditación Internacional, Curso de Ingles de la Corporación Universitaria Republicana, o Intercambio Internacional). Una vez el estudiante ha obtenido los respectivos certificados de los cursos de capacitación, exámenes de acreditación internacional, o intercambios académicos y/o culturales deberá surtir el siguiente procedimiento:
								<ul>
									<li><b>*</b> Presentar y radicar el certificado de acreditación respectivo ante su respectiva facultad acompañada del recibo de pago de la respectiva homologación expedido por la Oficina de Tesorería</li>
									<li><b>*</b> La facultad debe proceder a remitir el certificado de acreditación a el Departamento de Idiomas para la expedición de la respectiva acta de verificación de los requisitos requeridos de los estudiantes</li>
									<li><b>*</b> Una vez realizada el acta de verificación y homologación, el Departamento de Idiomas la remitirá de vuelta a la respectiva facultad y a la Oficina de Registro y Control</li>
									<li><b>*</b> Para acreditar la suficiencia en idiomas distintos al inglés, el estudiante deberá presentar ante el Departamento de Idiomas de la Corporación Universitaria Republicana un certificado expedido por una institución debidamente reconocida y habilitada para estos efectos, avalada por Entidades Oficiales o presentando el certificado del examen internacional en el cual se acrediten competencias razonablemente equivalentes a las establecidas en el presente comunicado para la lengua extranjera Inglés</li>
									<li><b>*</b> Cada estudiante deberá consultar ante su respectiva facultad de la Corporación Universitaria Republicana previo a la realización de un examen particular, la suficiencia de dicho examen, para satisfacer los requisitos de segundo o tercer idioma</li>
									<li><b>*</b> Es responsabilidad de cada estudiante cubrir los costos de capacitación dela segunda y tercera lengua extranjera así como los costos asociados con los exámenes de acreditación internacional</li>
								</ul>
